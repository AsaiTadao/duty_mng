<script>
import api, { apiErrorHandler } from '../../global/api';
import actionLoading from '../../mixin/actionLoading';
import { showSuccess } from '../../helpers/error';
import ShiftForm from './ShiftForm.vue';

    export default {
        mixins: [actionLoading],
        components: { ShiftForm },
        props: {
            data: {},
            offices: {},
            selectedOffices: {},
        },
        watch: {
            ['data.id'] : function (){
                    this.errors = {
                        name: '',
                };
            },
        },
        data() {
            return {
                errors: {
                    name: '',
                }
            }
        },

        methods: {
            saveRegion() {
                if (this.actionLoading) return;
                if (!this.validate()) return;
                this.setActionLoading();
                let request;
                if (this.data.id) {
                    request = api.post('region/' + this.data.id, null, this.data);
                } else {
                    request = api.post('region', null, this.data);
                }
                request.then(() => {
                        this.unsetActionLoading();
                        showSuccess(this.$t("Successfully saved"));
                        this.$emit('success');
                    })
                    .catch(e => {
                        apiErrorHandler(e);
                        this.unsetActionLoading();
                    });
            },
            validate() {
                let valid = true;
                if (!this.data.name) {
                    this.errors.name = this.$t('Please input name');                                 // need trans
                    valid = false;
                }
                return valid;
            },
            onOfficeChange(officeId) {
                const index = this.selectedOffices.findIndex(item => item.id == officeId);
                if(index != -1){
                    this.selectedOffices.splice(index, 1);
                }
            },
            openShiftPopup(employeeId, dateIndex){
                $('#shiftEditForm').modal('show');
                const employee = this.shifts.find(item => item.id === employeeId);
                if (!employee) return;
                const {id, name, employmentStatusId, roleId} = employee;
                this.selectedEmployee = {id, name, employmentStatusId, roleId};

                this.selectedDate = this.selectedMonth.slice(0,4) + '-' + this.selectedMonth.slice(4) + '-' + ('0' + (dateIndex + 1)).slice(-2);
                this.getWorkingHours(employeeId);
                this.selectedShift = this.shifts.filter(item => item.id == employeeId)[0].shifts[dateIndex];
                this.getVacations();
            },
        }
    }
</script>
